app.post('/api/orders', authenticateToken, async (req, res) => {
  try {
    const { restaurantId, items, paymentMethod, specialInstructions } = req.body;
    const userId = req.user.userId;

    const restaurant = db.restaurants.find(r => r.id === restaurantId);
    if (!restaurant) {
      return res.status(404).json({ error: 'Restaurant non trouv√©' });
    }

    const user = db.users.find(u => u.id === userId);
    if (!user) {
      return res.status(404).json({ error: 'Utilisateur non trouv√©' });
    }

    let total = 0;
    const orderItems = [];

    for (const item of items) {
      const menuItem = restaurant.menu.find(m => m.id === item.id);
      if (!menuItem) {
        return res.status(400).json({ error: `Article non trouv√©: ${item.id}` });
      }

      orderItems.push({
        id: menuItem.id,
        name: menuItem.name,
        price: menuItem.price,
        quantity: item.quantity || 1,
        customizations: item.customizations || []
      });

      total += menuItem.price * (item.quantity || 1);
    }

    const order = {
      id: ++db.orderCounter,
      userId,
      restaurantId,
      restaurant: {
        id: restaurant.id,
        name: restaurant.name,
        image: restaurant.image
      },
      student: {
        id: user.id,
        name: user.name,
        email: user.email
      },
      items: orderItems,
      total,
      paymentMethod,
      paymentStatus: paymentMethod === 'card' ? 'paid' : 'pending',
      specialInstructions: specialInstructions || '',
      status: 'pending',
      estimatedTime: 10,
      createdAt: new Date(),
      updatedAt: new Date()
    };

    // G√©n√©rer le QR code
    const qrData = {
      orderId: order.id,
      userId: userId,
      restaurantId: restaurantId,
      total: total,
      timestamp: new Date().toISOString()
    };

    const qrCodeUrl = await QRCode.toDataURL(JSON.stringify(qrData));
    order.qrCode = qrCodeUrl;

    db.orders.push(order);

    // Mettre √† jour les points de fid√©lit√©
    user.loyaltyPoints += Math.floor(total);
    user.totalOrders += 1;

    res.status(201).json({
      message: 'Commande cr√©√©e avec succ√®s',
      order: {
        id: order.id,
        restaurant: order.restaurant,
        items: order.items,
        total: order.total,
        status: order.status,
        estimatedTime: order.estimatedTime,
        qrCode: order.qrCode,
        createdAt: order.createdAt
      }
    });
  } catch (error) {
    console.error('Erreur cr√©ation commande:', error);
    res.status(500).json({ error: 'Erreur lors de la cr√©ation de la commande' });
  }
});

